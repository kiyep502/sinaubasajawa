public class AnakKewan {

    private Integer id;
    private String kewan;
    private String anakKewan;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getKewan() {
        return kewan;
    }

    public void setKewan(String kewan) {
        this.kewan = kewan;
    }

    public String getAnakKewan() {
        return anakKewan;
    }

    public void setAnakKewan(String anakKewan) {
        this.anakKewan = anakKewan;
    }
}
